    public static Accident getAccidentById(int nm){
  Accident ma=null;
    try{
        conn=getConnection();
        stm=conn.createStatement();
        rs=stm.executeQuery("select * from accident where IDAcc='"+nm+"'");
        if(rs.next()){
            ma=new Accident();
            ma.setTypeAcc(rs.getString("Type_Acc"));
            ma.setDate(rs.getString("Date_Acc"));
            ma.setLieu(rs.getString("lieu"));
            ma.setFo(rs.getInt("IDFO"));
        }
        conn.close();
        stm.close();
        return ma;
    }
    catch(Exception e){
        JOptionPane.showConfirmDialog(null, e.getMessage());
        return null;
    }
}
