public  void insererAccident(Accident cr){
    try{
        conn=getConnection();
       ps=conn.prepareStatement("insert into accident(Type_Acc,Date_Acc,lieu,IDFO) values(?,?,?,?)");
    ps.setString(1, cr.getTypeAcc());
     ps.setString(2,((JTextField)date.getDateEditor().getUiComponent()).getText());
    ps.setString(3, cr.getLieu());
     ps.setInt(4, cr.getFo());
    ps.executeUpdate();
    conn.close();
    }catch(Exception e){
        JOptionPane.showConfirmDialog(null, e.getMessage());
    } 
    }
